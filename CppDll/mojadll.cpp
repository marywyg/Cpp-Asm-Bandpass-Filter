#include <cstdint>

extern "C" void ModifyBitsCpp(float* chunkPointer, size_t numElements) {

    double filterCoefficients[] = {
        -0.0000, 0.0003, 0.0008, 0.0011, 0.0008, 0.0002, -0.0003, -0.0002, 0.0006, 0.0015,
        0.0015, 0.0004, -0.0011, -0.0018, -0.0008, 0.0011, 0.0021, 0.0007, -0.0027, -0.0055,
        -0.0053, -0.0020, 0.0012, 0.0007, -0.0046, -0.0112, -0.0138, -0.0100, -0.0029, 0.0002,
        -0.0053, -0.0169, -0.0258, -0.0240, -0.0121, -0.0006, -0.0017, -0.0181, -0.0385, -0.0452,
        -0.0294, -0.0016, 0.0136, -0.0037, -0.0483, -0.0873, -0.0786, -0.0022, 0.1208, 0.2344,
        0.2805, 0.2344, 0.1208, -0.0022, -0.0786, -0.0873, -0.0483, -0.0037, 0.0136, -0.0016,
        -0.0294, -0.0452, -0.0385, -0.0181, -0.0017, -0.0006, -0.0121, -0.0240, -0.0258, -0.0169
    };

    for (size_t i = 0; i < numElements; ++i) {
        float result = 0.0;
        for (size_t j = 0; j < 61; ++j) {
            if (i + j < numElements) {
                result += chunkPointer[i + j] * filterCoefficients[j];
            }
        }
        chunkPointer[i] = result;
    }
}